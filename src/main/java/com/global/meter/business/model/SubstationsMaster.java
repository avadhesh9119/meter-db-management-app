package com.global.meter.business.model;

import java.util.Date;
import java.util.Set;

import org.springframework.data.cassandra.core.mapping.PrimaryKey;
import org.springframework.data.cassandra.core.mapping.Table;

@Table(value = "substations_master")
public class SubstationsMaster {
	
	@PrimaryKey
	private int i_am_id;
	
	private String hes_substation_id;
	private String hes_subdivision_id;
	private String hes_owner_id;
	private String substation_name;
	private Set<String> access;
	private Date created;
	private String latitude;
	private String longitude;
	private Date modified;
	private String owner_name;
	private String subdivision_name;
	private String source;
	private String user_id;
	private String updated_by;
	private String address;
	
	public SubstationsMaster() {
		super();
		// TODO Auto-generated constructor stub
	}

	public SubstationsMaster(int i_am_id, String hes_substation_id, String hes_subdivision_id, String hes_owner_id,
			String substation_name, Set<String> access, Date created, String latitude, String longitude, Date modified,
			String owner_name, String subdivision_name, String source, String user_id, String updated_by,
			String address) {
		super();
		this.i_am_id = i_am_id;
		this.hes_substation_id = hes_substation_id;
		this.hes_subdivision_id = hes_subdivision_id;
		this.hes_owner_id = hes_owner_id;
		this.substation_name = substation_name;
		this.access = access;
		this.created = created;
		this.latitude = latitude;
		this.longitude = longitude;
		this.modified = modified;
		this.owner_name = owner_name;
		this.subdivision_name = subdivision_name;
		this.source = source;
		this.user_id = user_id;
		this.updated_by = updated_by;
		this.address = address;
	}

	public int getI_am_id() {
		return i_am_id;
	}

	public void setI_am_id(int i_am_id) {
		this.i_am_id = i_am_id;
	}

	public String getHes_substation_id() {
		return hes_substation_id;
	}

	public void setHes_substation_id(String hes_substation_id) {
		this.hes_substation_id = hes_substation_id;
	}

	public String getHes_subdivision_id() {
		return hes_subdivision_id;
	}

	public void setHes_subdivision_id(String hes_subdivision_id) {
		this.hes_subdivision_id = hes_subdivision_id;
	}

	public String getHes_owner_id() {
		return hes_owner_id;
	}

	public void setHes_owner_id(String hes_owner_id) {
		this.hes_owner_id = hes_owner_id;
	}

	public String getSubstation_name() {
		return substation_name;
	}

	public void setSubstation_name(String substation_name) {
		this.substation_name = substation_name;
	}

	public Set<String> getAccess() {
		return access;
	}

	public void setAccess(Set<String> access) {
		this.access = access;
	}

	public Date getCreated() {
		return created;
	}

	public void setCreated(Date created) {
		this.created = created;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public Date getModified() {
		return modified;
	}

	public void setModified(Date modified) {
		this.modified = modified;
	}

	public String getOwner_name() {
		return owner_name;
	}

	public void setOwner_name(String owner_name) {
		this.owner_name = owner_name;
	}

	public String getSubdivision_name() {
		return subdivision_name;
	}

	public void setSubdivision_name(String subdivision_name) {
		this.subdivision_name = subdivision_name;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getUpdated_by() {
		return updated_by;
	}

	public void setUpdated_by(String updated_by) {
		this.updated_by = updated_by;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	@Override
	public String toString() {
		return "SubstationsMaster [i_am_id=" + i_am_id + ", hes_substation_id=" + hes_substation_id
				+ ", hes_subdivision_id=" + hes_subdivision_id + ", hes_owner_id=" + hes_owner_id + ", substation_name="
				+ substation_name + ", access=" + access + ", created=" + created + ", latitude=" + latitude
				+ ", longitude=" + longitude + ", modified=" + modified + ", owner_name=" + owner_name
				+ ", subdivision_name=" + subdivision_name + ", source=" + source + ", user_id=" + user_id
				+ ", updated_by=" + updated_by + ", address=" + address + "]";
	}

}
