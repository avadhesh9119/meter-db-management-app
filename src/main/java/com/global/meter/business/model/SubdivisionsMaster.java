package com.global.meter.business.model;

import java.util.Date;
import java.util.Set;

import org.springframework.data.cassandra.core.mapping.PrimaryKey;
import org.springframework.data.cassandra.core.mapping.Table;

@Table(value = "subdivisions_master")
public class SubdivisionsMaster {

	@PrimaryKey
	private int i_am_id;
	private String hes_subdivision_id;
	private Set<String> access;
	private Date created;
	private String latitude;
	private String longitude;
	private Date modified;
	private String owner_id;
	private String source;
	private String subdivision_name;
	private String updated_by;
	private String user_id;
	private String owner_name;
	private String hes_zone_id;
	private String hes_circle_id;
	private String hes_division_id;
	private String zone_name;
	private String circle_name;
	private String division_name;
	private String subdivision_code;

	public SubdivisionsMaster() {
		super();
	}

	public SubdivisionsMaster(int i_am_id, String hes_subdivision_id, Set<String> access, Date created, String latitude,
			String longitude, Date modified, String owner_id, String source, String subdivision_name, String updated_by,
			String user_id, String owner_name, String hes_zone_id, String hes_circle_id, String hes_division_id,
			String zone_name, String circle_name, String division_name, String subdivision_code) {
		super();
		this.i_am_id = i_am_id;
		this.hes_subdivision_id = hes_subdivision_id;
		this.access = access;
		this.created = created;
		this.latitude = latitude;
		this.longitude = longitude;
		this.modified = modified;
		this.owner_id = owner_id;
		this.source = source;
		this.subdivision_name = subdivision_name;
		this.updated_by = updated_by;
		this.user_id = user_id;
		this.owner_name = owner_name;
		this.hes_zone_id = hes_zone_id;
		this.hes_circle_id = hes_circle_id;
		this.hes_division_id = hes_division_id;
		this.zone_name = zone_name;
		this.circle_name = circle_name;
		this.division_name = division_name;
		this.subdivision_code = subdivision_code;
	}

	public int getI_am_id() {
		return i_am_id;
	}

	public void setI_am_id(int i_am_id) {
		this.i_am_id = i_am_id;
	}

	public String getHes_subdivision_id() {
		return hes_subdivision_id;
	}

	public void setHes_subdivision_id(String hes_subdivision_id) {
		this.hes_subdivision_id = hes_subdivision_id;
	}

	public Set<String> getAccess() {
		return access;
	}

	public void setAccess(Set<String> access) {
		this.access = access;
	}

	public Date getCreated() {
		return created;
	}

	public void setCreated(Date created) {
		this.created = created;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public Date getModified() {
		return modified;
	}

	public void setModified(Date modified) {
		this.modified = modified;
	}

	public String getOwner_id() {
		return owner_id;
	}

	public void setOwner_id(String owner_id) {
		this.owner_id = owner_id;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getSubdivision_name() {
		return subdivision_name;
	}

	public void setSubdivision_name(String subdivision_name) {
		this.subdivision_name = subdivision_name;
	}

	public String getUpdated_by() {
		return updated_by;
	}

	public void setUpdated_by(String updated_by) {
		this.updated_by = updated_by;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public String getOwner_name() {
		return owner_name;
	}

	public void setOwner_name(String owner_name) {
		this.owner_name = owner_name;
	}

	public String getHes_zone_id() {
		return hes_zone_id;
	}

	public void setHes_zone_id(String hes_zone_id) {
		this.hes_zone_id = hes_zone_id;
	}

	public String getHes_circle_id() {
		return hes_circle_id;
	}

	public void setHes_circle_id(String hes_circle_id) {
		this.hes_circle_id = hes_circle_id;
	}

	public String getHes_division_id() {
		return hes_division_id;
	}

	public void setHes_division_id(String hes_division_id) {
		this.hes_division_id = hes_division_id;
	}

	public String getZone_name() {
		return zone_name;
	}

	public void setZone_name(String zone_name) {
		this.zone_name = zone_name;
	}

	public String getCircle_name() {
		return circle_name;
	}

	public void setCircle_name(String circle_name) {
		this.circle_name = circle_name;
	}

	public String getDivision_name() {
		return division_name;
	}

	public void setDivision_name(String division_name) {
		this.division_name = division_name;
	}

	public String getSubdivision_code() {
		return subdivision_code;
	}

	public void setSubdivision_code(String subdivision_code) {
		this.subdivision_code = subdivision_code;
	}

	@Override
	public String toString() {
		return "SubdivisionsMaster [i_am_id=" + i_am_id + ", hes_subdivision_id=" + hes_subdivision_id + ", access="
				+ access + ", created=" + created + ", latitude=" + latitude + ", longitude=" + longitude
				+ ", modified=" + modified + ", owner_id=" + owner_id + ", source=" + source + ", subdivision_name="
				+ subdivision_name + ", updated_by=" + updated_by + ", user_id=" + user_id + ", owner_name="
				+ owner_name + ", hes_zone_id=" + hes_zone_id + ", hes_circle_id=" + hes_circle_id
				+ ", hes_division_id=" + hes_division_id + ", zone_name=" + zone_name + ", circle_name=" + circle_name
				+ ", division_name=" + division_name + ", subdivision_code=" + subdivision_code + "]";
	}

}
