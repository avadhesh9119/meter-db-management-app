package com.global.meter.inventive.models;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_NULL)
public class OwnerDataRequest {

	private String email;
	private String ownerName;
	private String ownerCode;
	private String companyId;
	private String createdBy;
	private String updatedBy;
	private String isActive;
	private String level1Id;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getOwnerName() {
		return ownerName;
	}

	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

	public String getIsActive() {
		return isActive;
	}

	public void setIsActive(String isActive) {
		this.isActive = isActive;
	}

	public String getLevel1Id() {
		return level1Id;
	}

	public void setLevel1Id(String level1Id) {
		this.level1Id = level1Id;
	}

	public String getOwnerCode() {
		return ownerCode;
	}

	public void setOwnerCode(String ownerCode) {
		this.ownerCode = ownerCode;
	}

	@Override
	public String toString() {
		return "OwnerDataRequest [email=" + email + ", ownerName=" + ownerName + ", ownerCode=" + ownerCode
				+ ", companyId=" + companyId + ", createdBy=" + createdBy + ", updatedBy=" + updatedBy + ", isActive="
				+ isActive + ", level1Id=" + level1Id + "]";
	}

}
