package com.global.meter.inventive.models;

import java.io.Serializable;

public class Payload implements Serializable{
	
	
	private static final long serialVersionUID = 3961597423476795680L;
	
	private int batteryChargingStatus;
	private int powerGoodStatus;
	private int batteryRawValue;
	private int batteryMVValue;
	public int getBatteryChargingStatus() {
		return batteryChargingStatus;
	}
	public void setBatteryChargingStatus(int batteryChargingStatus) {
		this.batteryChargingStatus = batteryChargingStatus;
	}
	public int getPowerGoodStatus() {
		return powerGoodStatus;
	}
	public void setPowerGoodStatus(int powerGoodStatus) {
		this.powerGoodStatus = powerGoodStatus;
	}
	public int getBatteryRawValue() {
		return batteryRawValue;
	}
	public void setBatteryRawValue(int batteryRawValue) {
		this.batteryRawValue = batteryRawValue;
	}
	public int getBatteryMVValue() {
		return batteryMVValue;
	}
	public void setBatteryMVValue(int batteryMVValue) {
		this.batteryMVValue = batteryMVValue;
	}
	@Override
	public String toString() {
		return "Payload [batteryChargingStatus=" + batteryChargingStatus + ", powerGoodStatus=" + powerGoodStatus
				+ ", batteryRawValue=" + batteryRawValue + ", batteryMVValue=" + batteryMVValue + "]";
	}
}
