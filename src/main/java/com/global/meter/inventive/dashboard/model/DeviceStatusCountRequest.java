package com.global.meter.inventive.dashboard.model;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_NULL)
public class DeviceStatusCountRequest {

	private String totalMeter;
	private String installed;
	private String down;
	private String commissioned;
	private String faulty;
	private String inActive;
	private String todaysCommissioned;
	private String todaysInstalled;
	private String successCount;
	private String failureCount;

	public String getTotalMeter() {
		return totalMeter;
	}

	public void setTotalMeter(String totalMeter) {
		this.totalMeter = totalMeter;
	}

	public String getInstalled() {
		return installed;
	}

	public void setInstalled(String installed) {
		this.installed = installed;
	}

	public String getDown() {
		return down;
	}

	public void setDown(String down) {
		this.down = down;
	}

	public String getCommissioned() {
		return commissioned;
	}

	public void setCommissioned(String commissioned) {
		this.commissioned = commissioned;
	}

	public String getFaulty() {
		return faulty;
	}

	public void setFaulty(String faulty) {
		this.faulty = faulty;
	}

	public String getInActive() {
		return inActive;
	}

	public void setInActive(String inActive) {
		this.inActive = inActive;
	}

	public String getTodaysCommissioned() {
		return todaysCommissioned;
	}

	public void setTodaysCommissioned(String todaysCommissioned) {
		this.todaysCommissioned = todaysCommissioned;
	}

	public String getTodaysInstalled() {
		return todaysInstalled;
	}

	public void setTodaysInstalled(String todaysInstalled) {
		this.todaysInstalled = todaysInstalled;
	}

	public String getSuccessCount() {
		return successCount;
	}

	public void setSuccessCount(String successCount) {
		this.successCount = successCount;
	}

	public String getFailureCount() {
		return failureCount;
	}

	public void setFailureCount(String failureCount) {
		this.failureCount = failureCount;
	}
}