package com.global.meter.inventive.mdm.models;

import com.facebook.presto.jdbc.internal.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_NULL)
public class MdmMasterResponse {

	@JsonProperty("MDM Id")
	private String mdmId;
	
	@JsonProperty("FTP Address")
	private String ftpAddress;
	
	@JsonProperty("FTP Login")
	private String ftpLogin;
	
	@JsonProperty("FTP Password")
	private String ftpPassword;
	
	@JsonProperty("Is Active")
	private boolean isActive;
	
	@JsonProperty("MDM Id")
	private String loginId;
	
	@JsonProperty("MDM Auth Url")
	private String mdmAuthUrl;
	
	@JsonProperty("MDM Name")
	private String mdmName;
	
	@JsonProperty("Login Password")
	private String loginPassword;

	@JsonProperty("Created By")
	private String createdBy;
	
	@JsonProperty("Created On")
	private String createdOn;
	
	@JsonProperty("Updated By")
	private String updatedBy;
	
	@JsonProperty("Updated On")
	private String updatedOn;
	
	public String getMdmId() {
		return mdmId;
	}

	public void setMdmId(String mdmId) {
		this.mdmId = mdmId;
	}

	public String getFtpAddress() {
		return ftpAddress;
	}

	public void setFtpAddress(String ftpAddress) {
		this.ftpAddress = ftpAddress;
	}

	public String getFtpLogin() {
		return ftpLogin;
	}

	public void setFtpLogin(String ftpLogin) {
		this.ftpLogin = ftpLogin;
	}

	public String getFtpPassword() {
		return ftpPassword;
	}

	public void setFtpPassword(String ftpPassword) {
		this.ftpPassword = ftpPassword;
	}

	public boolean getIsActive() {
		return isActive;
	}

	public void setIsActive(boolean isActive) {
		this.isActive = isActive;
	}

	public String getLoginId() {
		return loginId;
	}

	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}

	public String getMdmAuthUrl() {
		return mdmAuthUrl;
	}

	public void setMdmAuthUrl(String mdmAuthUrl) {
		this.mdmAuthUrl = mdmAuthUrl;
	}

	public String getMdmName() {
		return mdmName;
	}

	public void setMdmName(String mdmName) {
		this.mdmName = mdmName;
	}

	public String getLoginPassword() {
		return loginPassword;
	}

	public void setLoginPassword(String loginPassword) {
		this.loginPassword = loginPassword;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public String getCreatedOn() {
		return createdOn;
	}

	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

	public String getUpdatedOn() {
		return updatedOn;
	}

	public void setUpdatedOn(String updatedOn) {
		this.updatedOn = updatedOn;
	}

}
