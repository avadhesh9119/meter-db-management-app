package com.global.meter.v3.inventive.models;

import java.io.Serializable;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_NULL)
public class ManufacturerMasterDataResponse implements Serializable {

	private static final long serialVersionUID = 6702781362986536106L;
	
	@JsonProperty("Code")
	private String code;
	
	@JsonProperty("IsActive")
	private Boolean isActive;
	
	@JsonProperty("Name")
	private String name;
	
	@JsonProperty("Created By")
	private String createdBy;
	
	@JsonProperty("Created On")
	private String createdOn;
	
	@JsonProperty("Updated By")
	private String updatedBy;
	
	@JsonProperty("Updated On")
	private String updatedOn;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public Boolean getIsActive() {
		return isActive;
	}

	public void setIsActive(Boolean isActive) {
		this.isActive = isActive;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public String getCreatedOn() {
		return createdOn;
	}

	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

	public String getUpdatedOn() {
		return updatedOn;
	}

	public void setUpdatedOn(String updatedOn) {
		this.updatedOn = updatedOn;
	}

	@Override
	public String toString() {
		return "ManufacturerMasterDataResponse [code=" + code + ", isActive=" + isActive + ", name=" + name
				+ ", createdBy=" + createdBy + ", createdOn=" + createdOn + ", updatedBy=" + updatedBy + ", updatedOn="
				+ updatedOn + "]";
	}

}
